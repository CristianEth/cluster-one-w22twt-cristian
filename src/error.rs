use cosmwasm_std::StdError;
use thiserror::Error;

#[derive(Error, Debug)]
pub enum ContractError {
    #[error("{0}")]
    Std(#[from] StdError),

    #[error("Custom Error val: {val:?}")]
    CustomError { val: String },

    #[error("Can't decrement by this amount")]
    InvalidDecrement {},

    #[error("No authorization for reset action")]
    Unauthorazied {},
}
